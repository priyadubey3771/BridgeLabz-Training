//creating class KilometersToMiles to convert the distance from kilometers to miles
public class KilometersToMiles {
    public static void main (String args[]) {
	   
	   //creating variables to store distance in kilometers and distance in miles 
       double Distance = 10.8 ;
	   double DistanceInMiles = Distance * 1.6 ;
	   
	   //Displaying distance in miles
	   System.out.println( "The distance 10.8 km in miles is " + DistanceInMiles) ;
	}
}